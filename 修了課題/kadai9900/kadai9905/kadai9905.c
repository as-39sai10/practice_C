  int arrow_D[8] = { 0,  16,  16,  16, 124,  56,  16,  0}; // ↓
  int arrow_L[8] = { 0,  16,  48, 126,  48,  16,   0,  0}; // ←
  int arrow_R[8] = { 0,   8,  12, 126,  12,   8,   0,  0}; // →
  int arrow_H[8] = { 0,  60, 126, 126, 126, 126,  60,  0}; // STOP
