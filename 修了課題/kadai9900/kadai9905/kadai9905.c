/*******************
  kadai9905.c
エレベータの上昇・下降時に表示される矢印のアニメを作成してみる。
起動後、カーソルキーを押す毎に、画面に↑(または↓←→)をスクロールしながら表示する。
↑は、8ｘ8ドットで構成され、このドットの範囲内でスクロールする。
Escで終了

[↑]の場合
↑がスクロールアップし、スクロールアウトした場合は、下辺からスクロールアウトした内容がスクロールアップする。

矢印のドットパターン例：
  [↑]     [↓]     [←]     [→]
........ ........ ........ ........
...*.... ...*.... ...*.... ....*...
..***... ...*.... ..**.... ....**..
.*****.. ...*.... .******. .******.
...*.... .*****.. ..**.... ....**..
...*.... ..***... ...*.... ....*...
...*.... ...*.... ........ ........
........ ........ ........ ........
Sleep(ミリ秒)使用、system("CLS")使用
キーコード一覧
 [↑]=72, [↓]=80, [←]=75, [→]=77

************************/
#define _CRT_SECURE_NO_WARNINGS

